async function main() {
    let added = 0,
        skipped = 0;
    for (const w of words) {
        const existing = await prisma.word.findFirst({
            where: { english: w.english, level: w.level },
        });
        if (existing) {
            skipped++;
            continue;
        }
        await prisma.word.create({
            data: {
                english: w.english,
                vietnamese: w.vietnamese,
                definition: w.definition,
                exampleSentence: w.exampleSentence,
                level: w.level,
                tags: w.tags,
            },
        });
        added++;
    }
    console.log(`C1 Seed: ${added} added, ${skipped} skipped`);
}

main()
    .catch(console.error)
    .finally(() => prisma.$disconnect());
