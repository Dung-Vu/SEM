async function main() {
    console.log("🌱 Seeding A1 expanded words...");
    const user = await prisma.user.findFirst();
    if (!user) {
        console.error("No user found");
        process.exit(1);
    }
    let count = 0;
    for (const w of words) {
        const existing = await prisma.word.findUnique({
            where: { english: w.english },
        });
        if (existing) continue;
        const word = await prisma.word.create({ data: w });
        await prisma.srsCard.create({
            data: {
                userId: user.id,
                wordId: word.id,
                intervalDays: 0,
                easeFactor: 2.5,
                nextReview: new Date(),
                status: "new",
            },
        });
        count++;
    }
    console.log(
        `✅ ${count} new A1 words seeded (${words.length} total in script)`,
    );
}

main()
    .catch((e) => {
        console.error(e);
        process.exit(1);
    })
    .finally(() => prisma.$disconnect());
